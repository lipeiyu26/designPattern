package com.lpy.design.desigenpattern.pattern23.iteratorPattern;

/**
 * 迭代器模式
 */
public class Course {
    private String name;

    public Course(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
